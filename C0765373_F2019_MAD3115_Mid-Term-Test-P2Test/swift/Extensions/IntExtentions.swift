
import Foundation


extension Int{
    
    func UnitHyrdoConvert() ->String {
        return "\(self) Units"
    }
    func convertToString() ->String {
        return "\(self)"
    }
    func internetUnit() -> String {
        return "\(self) gb"
    }
    
    func minutesUnits() -> String {
        return "\(self) minutes"
    }
}
